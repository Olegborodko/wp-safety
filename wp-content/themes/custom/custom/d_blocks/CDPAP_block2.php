                                  <p>
                                    The Consumer Directed Personal Assistance Program (CDPAP) is a statewide Medicaid program that provides an alternative way of receiving home care services, in which the consumer has more control over who provides his or her care and how it is provided.<br>This program enables consumers or their guardians to be self- directing and to assume the responsibility for their own care, giving the consumers more independence and allowing them to feel more autonomous.</p>
                                  <p>This Medicaid program provides services to chronically ill or physically disabled individuals who have medical needs and who require assistance in their activities of daily living (ADLs).The Consumer Directed Personal Assistance Program (CDPAP) is an alternative to traditional home care. It is a Medicaid program that enables self directing individuals or their administrators, to assume the responsibilities of their own care. CDPAP helps promote independence and Consumer Empowerment.</p>
                                  <p>In the State of New York, all of the counties and all Managed Long Term Care and Care plans must now provide this CDPAP option to all those who are entitled<br>It provides services to those people who may be chronically ill or physically disabled and have a need for medical help daily. The services include a home attendant, home health aide or even a registered nurse.</p>
                                  <p></p>
                                  <h5>Requirements of CDPAP:</h5>
                                  <p>Recipients or their designated person, must be able and willing to make informed decisions regarding the management of the services they receive.<br>The consumer or designee must also be responsible for recruiting, hiring, training, supervising- including payroll record keeping and terminating the caregiver’s service.<br>In the event that back-up coverage is needed, an alternative plan of care should be implemented by the consumer or designee.</p>
                                  <p></p>
                                  <h5>Duties of Personal Assistant Care givers</h5>
                                  <p>CDPAP aides have the special benefit of being allowed to perform “skilled” care that may otherwise only be performed by a nurse. These “skilled” tasks include, but are not limited to, suctioning tracheostomies, insulin injections, and the administration of oxygen and medication that cannot be self-administered. See N.Y. Educ. Law § 6908(1)(a). Tasks that cannot be performed by home health aides or personal care aides are indicated in the scope of tasks for home health aides and personal care.</p>
                                  <p></p>
                                  <h5 class="padding-top-8px">Advantages of CDPAP</h5>
                                  <p>Close Relatives even children over the age of 21 years can become a Personal Assistant. This provides a great sense of fulfillment as caregivers often want to do the best they can for their sick relative.<br>The Person who requires assistance can often be “difficult” in particular with strangers. The personal assistant has normally known the patient for a while and can deal with most situations. The patient is relaxed as is the caregiver.</p>
                                  <p>Often relatives want desperately to help their families but the cost of giving up a job to stay home and be the required care giver is impossible financially. This program pays the relative to do this!<br>The caregiver does not work for an agency. The state pays the wages and a company is appointed to authorize and manage this side.<br>The state also offers benefits to the caregiver such as health care and paid time off.<br>The pay is minimal but where a family member is already providing care, this little bit extra helps to make things a little easier.</p>
                                  <p></p>
                                  <h5>Who Can Be Hired As a CDPAP Aid?</h5>
                                  <p>The consumer can hire almost anyone, including some extended family members. New York State regulations prohibit one’s spouse from being hired as one’s CDPAP aide.<br>Any person hired must have valid work authorization and need not be “certified”, as training is done by the consumer or the consumer’s family.<br>New York State regulations prohibit one’s spouse or parent from being hired as one’s CDPAP aide, but makes an allowance for other family members. Any person hired must have valid work authorization and need not be “certified”, as the training is done by the consumer or the consumer’s family.</p>
                                  <p></p>
                                  <h5 class="padding-top-8px">What are the duties of the State of New York Health Department.</h5>
                                  <p>Processing and liaising with appointed private companies to pay Personal assistants weekly.<br>Health Benefits for Personal Assistants Co coordinating leave<br>Providing where necessary Nursing Supervision<br>Providing help and guidance with recruiting and training<br>They create an advisory committee that will make sure that the needed quality of services by Personal Asistants is provided.<br>Providing ongoing training.</p>
                                  <p>The CPDAD is a wonderful program which as its main advantages, allows patients and their families to have control over their personal medical assistants ,to train and even firing personnel within the comfort of their own home . Allowing freedom and dignity.</p>
                                  <p></p>
                                  <h5>Application and Procedures</h5>
                                  <p>1.	Enrollment Registration</p>
                                  <p>2.	The consumer (patient) completes an application;</p>
                                  <p>3.	The consumer’s designated caregiver completes an CDPAP application packet and submits all required documents directly to the SAFETY 1ST HOMECARE, INC.   FI office;</p>
                                  <p>4.	Any other caregiver who would like to be listed on the SAFETY 1ST HOMECARE, INC.   FI caregiver database and be chosen by CDPAP consumers can complete an application and submit any required documents directly to SAFETY 1ST HOMECARE, INC.  FI office.</p>
                                  <p>5.	The consumer or his/her representative chooses and confirms the choice of the aides.</p>
                                  <p>6.	Safety 1st HomeCare, Inc.  FI submits the application to the MLTC plan that the consumer requests.</p>
                                  <p>7.	The MLTC plan sends an enrollment nurse to the consumer’s home for a nursing assessment to determine the eligibility of the consumer. A nurse assessor determines whether the recipient can appropriately participate in CDPAP, and recommends the amount, frequency and duration of services.</p>
                                  <p>8.	The MLTC plan submits all enrollment applications to HRA/Medicaid for approval.</p>
                                  <p>9.	Upon approval, SAFETY 1ST HOMECARE, INC.   FI receives authorization from the MLTC Plan. SAFETY 1ST HOMECARE, INC.   FI will inform the consumer (patient) and then authorize the aide to start the service.</p>
                                  <p>10.	Aides must submit all duty sheets to SAFETY 1ST HOMECARE, INC.  FI.</p>
                                  <p>11.	SAFETY 1ST HOMECARE, INC.  FI processes the payroll based on the duty sheets and pays the aides’ wages and benefits.
