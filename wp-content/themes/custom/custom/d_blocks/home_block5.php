            <h5 class="tm_pb_toggle_title">What is CDPAP (Consumer Directed Personal Assistance Program)</h5>
            <div class="tm_pb_toggle_content clearfix">

              <p>The Consumer Directed Personal Assistance Program (CDPAP) is a statewide Medicaid program that provides
                an alternative way of receiving home care services, in which the consumer has more control over who
                provides his or her care and how it is provided. This program enables consumers or their guardians to be
                self-directing and to assume the responsibility for their own care, giving the consumers more
                independence and allowing them to feel more autonomous.
              </p>
