            <h5 class="tm_pb_toggle_title">Who is Eligible to be part of the CDPAP Program?</h5>
            <div class="tm_pb_toggle_content clearfix">

              <p></p>
              <p>There are three points that must be noted to be eligible to apply for participation in the CDPAP
                program.</p>
              <ul>
                <li>Must be a member of Medicaid.</li>
                <li>Must actually need home care.</li>
                <li>Recipients, their legal guardians or other persons designated to help and guide them must be able
                  and willing to make informed decisions regarding the management of the services they receive.
                </li>
                <li>The consumer or designee must also be responsible for recruiting, hiring, training,
                  supervising-including payroll record keeping and terminating the caregiver’s service. In the event
                  that back-up coverage is needed, an alternative plan of care should be implemented by the consumer or
                  designee.
                </li>
              </ul>

