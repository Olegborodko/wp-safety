          <div class="tm_pb_module tm_pb_toggle tm_pb_toggle_close  tm_pb_accordion_item_2">
            <h5 class="tm_pb_toggle_title">How to apply to be part of the CDPAP program</h5>
            <div class="tm_pb_toggle_content clearfix">

              <p>1. Enrollment Registration</p>
              <p>2. The consumer (patient) completes an application;</p>
              <p>3. The consumer’s designated caregiver completes an CDPAP application packet and submits all required
                documents directly to the SAFETY 1ST HOMECARE, INC. FI office;</p>
              <p>4. Any other caregiver who would like to be listed on the SAFETY 1ST HOMECARE, INC. FI caregiver
                database and be chosen by CDPAP consumers can complete an application and submit any required documents
                directly to SAFETY 1ST HOMECARE, INC. FI office.</p>
              <p>5. The consumer or his/her representative chooses and confirms the choice of the aides.</p>
              <p>6. Safety 1st HomeCare, Inc. FI submits the application to the MLTC plan that the consumer
                requests.</p>
              <p>7. The MLTC plan sends an enrollment nurse to the consumer’s home for a nursing assessment to determine
                the eligibility of the consumer. A nurse assessor determines whether the recipient can appropriately
                participate in CDPAP, and recommends the amount, frequency and duration of services.</p>
              <p>8. The MLTC plan submits all enrollment applications to HRA/Medicaid for approval.</p>
              <p>9. Upon approval, SAFETY 1ST HOMECARE, INC. FI receives authorization from the MLTC Plan. SAFETY 1ST
                HOMECARE, INC. FI will inform the consumer (patient) and then authorize the aide to start the
                service.</p>
              <p>10. Aides must submit all duty sheets to SAFETY 1ST HOMECARE, INC. FI.</p>
              <p>11. SAFETY 1ST HOMECARE, INC. FI processes the payroll based on the duty sheets and pays the aides’
                wages and benefits.</p>

